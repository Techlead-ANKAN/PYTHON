# CREATE a list using []
a = [2,5,18,98,50]

# PRINT the list using print() function
print(a)

# ACCESS any item by entering its Index. Like a[0], a[1], a[2] etc.
print(a[2])

# CHANGE any value of list like this:   variable[index of the item u want to change] = new item
                                        # print(variable)
a[2] = 10
print(a)

# # We can create a list of different data types: int,float,string,boolean
b = [20, 50.25, "ANKAN", False]
print(b)


