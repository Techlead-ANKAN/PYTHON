# Python automatically identifies the type of the variables.

# Note: - IN STRING - SINGLE, DOUBLE AND TRIPLE QUOTES CAN BE USED. TRIPLE IS USED FOR MULTI-LINE STRING

a = "ANKAN" # String (str)
b = 19      # Integer (int)
c = 11.91   # Float (float)
d = True    # Boolean (bool)
e = False   # Boolean (bool)
f = None    # None (NoneType)

# Printing the variables
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)

# Printing the Data-type of variables
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))


